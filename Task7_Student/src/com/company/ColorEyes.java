package com.company;

public class ColorEyes {
    private String colorEyes;

    public ColorEyes(String colorEyes) {
        this.colorEyes = colorEyes;
    }

    public ColorEyes() {
        this.colorEyes = "Blue";
    }

    public void setColorEyes(String colorEyes) {
        this.colorEyes = colorEyes;
    }

    public String getColorEyes() {
        return colorEyes;
    }
}
